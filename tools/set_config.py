import paho.mqtt.client as mqtt
import json

# MQTT Broker details
broker_address = "test.monstermq.com"
port = 1883

# Create MQTT client
client = mqtt.Client()

def on_connect(client, userdata, flags, rc):
    if rc == 0:
        print("Connected to MQTT Broker!")
        # Loop through the dictionary and publish messages
        for topic, data in j.items():
            payload = json.dumps(data)
            client.publish("Vogler/home/"+topic + "/<config>", payload, retain=True)
            print(f"Published config for topic: {topic} => {payload}")
    else:
        print("Failed to connect, return code %d\n", rc)

# Connect to MQTT broker
client.on_connect = on_connect
client.connect(broker_address, port)
client.loop_forever()

